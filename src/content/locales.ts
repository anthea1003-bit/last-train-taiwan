export function translate(key: string, lang: Language): string {
  const dict = LOCALES[lang] || LOCALES['zh-TW'];
  if (key in dict) {
    return dict[key];
  }

  // Fallback to Traditional Chinese
  const fallbackDict = LOCALES['zh-TW'];
  if (key in fallbackDict) {
    console.warn(`[Locale Warning]: Missing localization key "${key}" for language "${lang}". Falling back to zh-TW.`);
    return fallbackDict[key];
  }

  console.warn(`[Locale Warning]: Key "${key}" not found in any language dictionaries.`);
  return key;
}
